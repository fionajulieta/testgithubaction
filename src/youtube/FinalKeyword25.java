package youtube;

public class FinalKeyword25 {
    public static void main(String[] args) {

        // final variable : to state an unchangeable variable value
        final double PI = 3.14159;

        // if we attempt to assign new value to the variable as below:
        // PI = 4;
        // the output will display an error -> java: cannot assign a value to final variable PI

        System.out.println(PI);

    }
}
