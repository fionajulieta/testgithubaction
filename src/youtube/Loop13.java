package youtube;

public class Loop13 {
    public static void main(String[] args) {

        // for done.loop = executes a block of code a limited amount of times

        for(int i=10; i>=0; i--) {
            System.out.println(i);
        }
        // i=10, will continuously minus 1 (i--) as long as i is not equal to 0

        System.out.println("Happy new year!");
    }
}
